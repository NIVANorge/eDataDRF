        actions = actions
      ) |>
      col_vals_in_set(
        columns = SAMPLE_TISSUE,
        set = tissue_types_vocabulary(),
        actions = actions
      ) |>
      col_vals_in_set(
        columns = SAMPLE_SPECIES_LIFESTAGE,
        set = lifestage_vocabulary(),
        actions = actions
      ) |>
      col_vals_in_set(
        columns = SAMPLE_SPECIES_GENDER,
